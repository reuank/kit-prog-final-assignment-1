package edu.kit.informatik.constructs.program;

/**
 * Used to encode directions on the board in a readable way.
 */
public enum Direction {
    /**
     * The up-direction.
     */
    UP,

    /**
     * The down-direction.
     */
    DOWN,

    /**
     * The left-direction.
     */
    LEFT,

    /**
     * The right-direction.
     */
    RIGHT
}